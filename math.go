package mymath

import "math"

func Sqrt(a float64) float64 {
	return math.Sqrt(a)
}

func Ceil(a float64) float64 {
	return math.Ceil(a)
}

func Floor(a float64) float64 {
	return math.Floor(a)
}

func Pow(a, b float64) float64 {
	return math.Pow(a, b)
}

func Max(a, b float64) float64 {
	return math.Max(a, b)
}

func Min(a, b float64) float64 {
	return math.Min(a, b)
}